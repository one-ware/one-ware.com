function addIconsToItems(items: readonly any[]): any[] {
  return items.map((item) => {
    if (item.type === 'html') {
      return item;
    }

    const label = item.label || '';
    const icon = navbarIcons[label];

    if (icon) {
      return {
        ...item,
        label: (
          <>
            {icon}
            <span>{label}</span>
          </>
        ),
      };
    }

    return item;
  });
}

export default function DropdownNavbarItemWrapper(props: Props): JSX.Element {
  const modifiedProps = {
    ...props,
    items: addIconsToItems(props.items || []),
  };

  return <DropdownNavbarItem {...modifiedProps} />;
}
